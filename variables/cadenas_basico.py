# Guardamos un texto en una variable
mensaje = "Hola Mundo"

# Mostrar en mayúsculas
print("Mayúsculas:", mensaje.upper())

# Mostrar en minúsculas
print("Minúsculas:", mensaje.lower())

# Contar letras 'o'
print("Cantidad de 'o':", mensaje.count('o'))

# Reemplazar texto "Mundo" por "Python"
print("Reemplazar 'Mundo' por 'Python':", mensaje.replace("Mundo", "Python"))
